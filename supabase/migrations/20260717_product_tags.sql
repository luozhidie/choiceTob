-- ============================================================
-- 20260717 商品自定义展示标签（营销标签）
-- 用途：后台商品编辑「商品标签（逗号分隔）」写入的列
--       详情页读取 product.tags 原样展示（如 今日新款 / 沙河货源 / 清仓）
-- 请在 Supabase SQL Editor 执行（Vercel 不会自动跑迁移）
-- 幂等：可重复执行
-- ============================================================

ALTER TABLE products ADD COLUMN IF NOT EXISTS tags TEXT[];
COMMENT ON COLUMN products.tags IS '商品自定义展示标签（逗号分隔存入数组），如 今日新款/沙河货源/清仓';
